  function getStateList() {
	document.getElementById('accordion').innerHTML= '<div class="d-flex justify-content-center"><div class="spinner-border text-warning" role="status"> <span class="sr-only">Loading...</span> </div> </div>';
   //https://api.covid19india.org/state_district_wise.json     
   $.getJSON("https://api.covid19india.org/state_district_wise.json", function (data) {
      var stateData = [];
      stateData["content"] = data;
      document.getElementById('accordion').innerHTML = tmpl("tmpl-accordion", stateData);

      document.getElementById('KriShInput').addEventListener('input', function () {
      var input, filter, ul, li, a, i, txtValue;
      filter = this.value.toUpperCase();
      
      ul = document.getElementById("KriSh");
      li = ul.getElementsByTagName("li");
      for (i = 0; i < li.length; i++) {
         a = li[i].getElementsByTagName("div")[1];
         txtValue = a.textContent || a.innerText;

         if (txtValue.toUpperCase().indexOf(filter) > -1 ) {
            li[i].style.display = "";
            
            pera = a.getElementsByTagName("cont");
            for (j = 0; j < pera.length; j++) {
               ank = pera[j].getElementsByTagName("a")[0];
               inrtxt = ank.textContent || ank.innerText;
               if (inrtxt.toUpperCase().indexOf(filter) > -1) {
                  pera[j].style.display = "";
                  li[i].children[1].setAttribute("class","panel-collapse collapse show");
               } else {
                  pera[j].style.display = "none";
               }
            }
            
         } else {
            li[i].style.display = "none";
            li[i].children[1].setAttribute("class","panel-collapse collapse");
         }
      }
      });

    });
  }


